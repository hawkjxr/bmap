// ─── 辅助函数 ─────────────────────────────────────────────────

/** 根据企业ID查找企业 */
export function getFirmById(id: string): Firm | undefined {
  return firmData.find(f => f.id === id);
}

/** 根据客户经理ID查找其管户企业 */
export function getFirmsByManagerId(managerId: string): Firm[] {
  return firmData.filter(f => f.managerId === managerId);
}

/** 根据园区ID查找园区内企业 */
export function getFirmsByParkId(parkId: string): Firm[] {
  return firmData.filter(f => f.parkIds.includes(parkId));
}

/** 根据企业名称模糊搜索 */
export function searchFirms(keyword: string): Firm[] {
  const kw = keyword.toLowerCase();
  return firmData.filter(f =>
    f.name.toLowerCase().includes(kw) ||
    f.fullName.toLowerCase().includes(kw) ||
    (f.industry && f.industry.toLowerCase().includes(kw))
  );
}

/** 企业 ID → 名称 索引 */
export const firmIdToName: Record<string, string> = Object.fromEntries(
  firmData.map(f => [f.id, f.name])
);

/** 企业 ID → 园区列表 索引 */
export const firmIdToParkIds: Record<string, string[]> = Object.fromEntries(
  firmData.map(f => [f.id, f.parkIds])
);
